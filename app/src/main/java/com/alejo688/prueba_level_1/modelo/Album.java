package com.alejo688.prueba_level_1.modelo;

public class Album {

    public String name;
    public String listeners;
    public String mbid;
    public String url;
    public String artistName;
    public String artistUrl;

    public Album (String name, String listeners, String mbid, String url, String artistName, String artistUrl) {
        this.name = name;
        this.listeners = listeners;
        this.mbid = mbid;
        this.url = url;
        this.artistName = artistName;
        this.artistUrl = artistUrl;
    }

}
